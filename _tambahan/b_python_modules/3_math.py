# MATH METHODS
# ┌────────────────────────────┬─────────────────────────────────────────────────────────────────────┐
# │           Method           │ Deskripsi                                                           │
# ├────────────────────────────┼─────────────────────────────────────────────────────────────────────┤
# │ math.acos(x)               │ Arc cosine dari x                                                   │
# │ math.acosh(x)              │ Invers hiperbolik cosine dari x                                     │
# │ math.asin(x)               │ Arc sine dari x                                                     │
# │ math.asinh(x)              │ Invers hiperbolik sine dari x                                       │
# │ math.atan(x)               │ Arc tangent dari x (dalam radian)                                   │
# │ math.atan2(y, x)           │ Arc tangent dari y/x (dalam radian, mempertimbangkan kuadran)       │
# │ math.atanh(x)              │ Invers hiperbolik tangent dari x                                    │
# │ math.ceil(x)               │ Membulatkan ke atas ke integer terdekat                             │
# │ math.comb(n, k)            │ Kombinasi memilih k dari n tanpa urutan dan duplikasi               │
# │ math.copysign(x, y)        │ Float dengan nilai x dan tanda dari y                               │
# │ math.cos(x)                │ Cosine dari x (dalam radian)                                        │
# │ math.cosh(x)               │ Cosine hiperbolik dari x                                            │
# │ math.degrees(x)            │ Konversi radian ke derajat                                          │
# │ math.dist(p, q)            │ Jarak Euclidean antara dua titik                                    │
# │ math.erf(x)                │ Fungsi error                                                        │
# │ math.erfc(x)               │ Fungsi error komplemen                                              │
# │ math.exp(x)                │ Nilai e^x                                                           │
# │ math.expm1(x)              │ e^x - 1, akurat untuk x kecil                                       │
# │ math.fabs(x)               │ Nilai absolut float dari x                                          │
# │ math.factorial(x)          │ Faktorial dari x                                                    │
# │ math.floor(x)              │ Membulatkan ke bawah ke integer terdekat                            │
# │ math.fmod(x, y)            │ Sisa pembagian x / y                                                │
# │ math.frexp(x)              │ Mantisa dan eksponen x (x = m × 2^e)                                │
# │ math.fsum(iterable)        │ Penjumlahan presisi tinggi dari iterable                            │
# │ math.gamma(x)              │ Fungsi gamma dari x                                                 │
# │ math.gcd(x, y)             │ Faktor persekutuan terbesar dari x dan y                            │
# │ math.hypot(*coords)        │ Norma Euclidean (√x² + y² + ...) dari koordinat                     │
# │ math.isclose(a, b)         │ True jika a dan b cukup dekat                                       │
# │ math.isfinite(x)           │ True jika x bukan infinity atau NaN                                 │
# │ math.isinf(x)              │ True jika x adalah infinite                                         │
# │ math.isnan(x)              │ True jika x adalah NaN                                              │
# │ math.isqrt(x)              │ Akar kuadrat integer dari x                                         │
# │ math.ldexp(x, i)           │ Nilai x × (2^i), kebalikan dari `frexp()`                           │
# │ math.lgamma(x)             │ Logaritma dari gamma(x)                                             │
# │ math.log(x[, base])        │ Logaritma natural atau log basis tertentu                           │
# │ math.log10(x)              │ Logaritma basis 10 dari x                                           │
# │ math.log1p(x)              │ Log(1+x), akurat untuk x kecil                                      │
# │ math.log2(x)               │ Logaritma basis 2 dari x                                            │
# │ math.perm(n, k)            │ Permutasi memilih k dari n dengan urutan tanpa duplikasi            │
# │ math.pow(x, y)             │ x pangkat y                                                         │
# │ math.prod(iterable)        │ Perkalian semua elemen dalam iterable                               │
# │ math.radians(x)            │ Konversi derajat ke radian                                          │
# │ math.remainder(x, y)       │ Nilai sisa terdekat agar x habis dibagi y                           │
# │ math.sin(x)                │ Sine dari x                                                         │
# │ math.sinh(x)               │ Sine hiperbolik dari x                                              │
# │ math.sqrt(x)               │ Akar kuadrat dari x                                                 │
# │ math.tan(x)                │ Tangent dari x                                                      │
# │ math.tanh(x)               │ Tangent hiperbolik dari x                                           │
# │ math.trunc(x)              │ Bagian integer dari x (tanpa pembulatan)                            │
# └────────────────────────────┴─────────────────────────────────────────────────────────────────────┘



