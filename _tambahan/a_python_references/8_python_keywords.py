# ┌────────────────┬─────────────────────────────────────────────────────────────────────────┐
# │    Keyword     │ Deskripsi                                                               │
# ├────────────────┼─────────────────────────────────────────────────────────────────────────┤
# │ as             │ Membuat alias (nama lain), misalnya saat import                         │
# │ assert         │ Digunakan untuk debugging: memverifikasi kondisi                        │ 
# │ break          │ Keluar dari loop (for/while) secara paksa                               │
# │ class          │ Mendefinisikan sebuah kelas                                             │
# │ continue       │ Lompat ke iterasi selanjutnya dalam loop                                │
# │ def            │ Mendefinisikan fungsi                                                   │
# │ del            │ Menghapus objek                                                         │
# │ except         │ Menangani exception (kesalahan) yang terjadi                            │
# │ finally        │ Blok kode yang selalu dijalankan dalam penanganan exception             │
# │ from           │ Import sebagian dari modul                                              │
# │ global         │ Mendeklarasikan variabel global di dalam fungsi                         │
# │ import         │ Mengimpor modul eksternal                                               │
# │ in             │ Mengecek apakah suatu nilai terdapat dalam koleksi (list, tuple, dsb)   │
# │ lambda         │ Membuat fungsi anonim (tanpa nama)                                      │
# │ None           │ Menyatakan nilai kosong / tidak bernilai                                │
# │ nonlocal       │ Mendeklarasikan variabel dari scope luar fungsi                         │
# │ pass           │ Pernyataan kosong; tidak melakukan apa-apa                              │
# │ raise          │ Melempar exception (kesalahan)                                          │
# │ return         │ Keluar dari fungsi dan mengembalikan nilai                              │
# │ try            │ Membuat blok percobaan penanganan exception                             │
# │ with           │ Menyederhanakan manajemen resource dan exception handling               │
# │ yield          │ Menghasilkan nilai dari generator (fungsi yang bersifat iterable)       │
# └────────────────┴─────────────────────────────────────────────────────────────────────────┘
