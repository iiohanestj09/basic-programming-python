# ┌──────────────────────┬──────────────────────────────────────────────────────────────┐
# │        Method        │ Deskripsi                                                    │
# ├──────────────────────┼──────────────────────────────────────────────────────────────┤
# │ capitalize()         │ Mengubah huruf pertama menjadi huruf kapital                 │
# │ casefold()           │ Mengubah string menjadi huruf kecil seluruhnya               │
# │ center()             │ Mengembalikan string yang diposisikan di tengah              │
# │ count()              │ Menghitung jumlah kemunculan substring tertentu              │
# │ encode()             │ Mengubah string jadi versi ter-encode                        │
# │ endswith()           │ True jika string diakhiri dengan nilai tertentu              │
# │ expandtabs()         │ Mengatur ukuran tab di dalam string                          │
# │ find()               │ Mencari posisi substring pertama yang ditemukan              │
# │ format()             │ Memformat string menggunakan nilai yang ditentukan           │
# │ format_map()         │ Memformat string menggunakan dictionary                      │
# │ index()              │ Mirip find(), tapi error kalau tidak ditemukan               │
# │ isalnum()            │ True jika semua karakter alfanumerik                         │
# │ isalpha()            │ True jika semua karakter huruf                               │
# │ isascii()            │ True jika semua karakter ASCII                               │
# │ isdecimal()          │ True jika semua karakter adalah angka desimal                │
# │ isdigit()            │ True jika semua karakter adalah digit                        │
# │ isidentifier()       │ True jika string bisa jadi nama variabel                     │
# │ islower()            │ True jika semua karakter huruf kecil                         │
# │ isnumeric()          │ True jika semua karakter bernilai angka                      │
# │ isprintable()        │ True jika semua karakter bisa dicetak                        │
# │ isspace()            │ True jika semua karakter adalah spasi atau whitespace        │
# │ istitle()            │ True jika string mengikuti format judul (Awal Kapital)       │
# │ isupper()            │ True jika semua karakter huruf besar                         │
# │ join()               │ Gabungkan iterable menjadi string                            │
# │ ljust()              │ Kiri justify string dengan padding                           │
# │ lower()              │ Mengubah semua karakter jadi huruf kecil                     │
# │ lstrip()             │ Menghapus spasi di bagian kiri string                        │
# │ maketrans()          │ Membuat tabel terjemahan untuk translate()                   │
# │ partition()          │ Pisahkan string jadi 3 bagian (sebelum, pemisah, sesudah)    │
# │ replace()            │ Mengganti bagian string dengan nilai baru                    │
# │ rfind()              │ Cari substring dari akhir string (posisi terakhir)           │
# │ rindex()             │ Sama seperti rfind() tapi error jika tidak ditemukan         │
# │ rjust()              │ Kanan justify string dengan padding                          │
# │ rpartition()         │ Pisahkan string dari belakang jadi 3 bagian                  │
# │ rsplit()             │ Pisahkan string dari kanan                                   │
# │ rstrip()             │ Menghapus spasi dari kanan string                            │
# │ split()              │ Memecah string berdasarkan pemisah tertentu                  │
# │ splitlines()         │ Memecah string berdasarkan baris                             │
# │ startswith()         │ True jika string dimulai dengan nilai tertentu               │
# │ strip()              │ Menghapus spasi di kiri dan kanan                            │
# │ swapcase()           │ Menukar kapital: kecil → besar, besar → kecil                │
# │ title()              │ Huruf pertama setiap kata jadi kapital                       │
# │ translate()          │ Terjemahkan string berdasarkan tabel `maketrans()`           │
# │ upper()              │ Mengubah string jadi huruf kapital semua                     │
# │ zfill()              │ Tambahkan nol di depan hingga panjang tertentu               │
