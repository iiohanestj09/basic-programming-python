# ┌─────────────────────────┬──────────────────────────────────────────────────────────────────┐
# │        Method           │ Deskripsi                                                        │
# ├─────────────────────────┼──────────────────────────────────────────────────────────────────┤
# │ close()                 │ Menutup file yang dibuka                                         │
# │ detach()                │ Mengembalikan stream mentah yang terpisah dari buffer            │
# │ fileno()                │ Mengembalikan nomor stream dari perspektif sistem operasi        │
# │ flush()                 │ Membersihkan (flush) buffer internal                             │
# │ isatty()                │ True jika stream file bersifat interaktif (misal: terminal)      │
# │ read()                  │ Membaca isi file secara keseluruhan                              │
# │ readable()              │ True jika file dapat dibaca                                      │
# │ readline()              │ Membaca satu baris dari file                                     │
# │ readlines()             │ Membaca semua baris dan mengembalikan dalam bentuk list          │
# │ seek(offset)            │ Mengubah posisi baca/tulis dalam file                            │
# │ seekable()              │ True jika posisi file dapat diubah (via seek)                    │
# │ tell()                  │ Mengembalikan posisi saat ini dalam file                         │
# │ truncate(size)          │ Mengubah ukuran file menjadi nilai tertentu                      │
# │ writable()              │ True jika file dapat ditulis                                     │
# │ write(string)           │ Menulis string ke dalam file                                     │
# │ writelines(list)        │ Menulis list string ke file (tanpa \n otomatis)                  │
# └─────────────────────────┴──────────────────────────────────────────────────────────────────┘
