# 1. Tambah, Kurang, Kali, Bagi, Modulus, Eksponen, Floor Division
a = 5
print("Nilai a =", a)
a += 4
print("a += 4, Menjadi", a)

a -= 3
print("a -= 3, Menjadi", a)

a *= 2
print("a *= 2, Menjadi", a)

a /= 6
print("a /= 6, Menjadi", a)

a %= 7
print("a %= 7, Menjadi", a)

a **= 8
print("a **= 8, Menjadi", a)

a //= 9
print("a //= 9, Menjadi", a, "\n")

# 2. Operasi Bitwise (OR, AND, XOR) NOT XXX
b = True
print("Nilai b =", b)

b |= True
print("b |= True, Menjadi", b)

b &= False
print("b &= False, Menjadi", b)

b ^= True
print("b ^= True, Menjadi", b, "\n")

# 3. SHIFTING
c = 10
print("Nilai c =", c, "Atau", format(c,'04b'))

c >>= 2
print("c >>= 2, Menjadi", c, "Atau", format(c,'04b'))

c <<= 1
print("c <<= 1, Menjadi", c, "Atau", format(c,'04b'))



