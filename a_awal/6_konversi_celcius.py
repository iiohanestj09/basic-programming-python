dataC = float(input("Masukan Nilai Derajat Celcius : "))
print("Diketahui, Data =", dataC, "C\n")

#Reamur
dataR = (4 / 5) * dataC
print("Reamur = ", dataR, "R")

#Fahrenheit
dataF = ((9 / 5) * dataC) + 32
print("Fahrenheit = ", dataF, "F")

#Kelvin
dataK = dataC + 273
print("Kelvin = ", dataK, "K")